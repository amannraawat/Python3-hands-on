# \t is used for assigning five charcaters empty space
tabby_cat = "\tI'm tabbed in."

# \n is used for stoping control from specific line and moving control to the next line 
persian_cat = "I'm split\non a line."

# backslash character(\) is used to ignore something if we want single quotes, double quotes or anything in our string
backslash_cat = "I'm \\ a \\ cat."

# triple quotes to print multiple lines string
fat_cat = """
I'll do a list:
\t\t* Cat food
\t\t\t* Fishies
\t\t\t\t* Catnip\n\t\t\t\t\t* Grass
"""

print(tabby_cat)
print(persian_cat)
print(backslash_cat)
print(fat_cat)
